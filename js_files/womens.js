function sorting() {
    let prices = document.querySelector("#sortby").value;
    console.log(prices)
    if (prices == "hightolow") {
        womenData.sort(function (a, b) {
            return Number(b.price) - Number(a.price);
        });
        displayDataWomen(womenData)
    }
    if (prices == "lowtohigh") {
        womenData.sort(function (a, b) {
            return Number(a.price) - Number(b.price);
        })
        displayDataWomen(womenData)
    }
}


