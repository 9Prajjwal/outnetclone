let productList =  [];


function displayDataWomen(womenData) {
    

    document.querySelector("#products").innerHTML = ""
    womenData.map(function (elem) {
        let maindiv = document.createElement("div")
        maindiv.setAttribute("class", "maindiv")

        let div1 = document.createElement("div")
        div1.setAttribute("class", "priceDiv")

        let div2 = document.createElement("div")
        div2.setAttribute("id", "infoDiv")

        let image = document.createElement("img")
        image.src=elem.img_2
        image.addEventListener("click", function () {
            moreInfo(elem);
            window.location.href = "product.html";
        })

        image.addEventListener("mouseenter",function(){
            image.src=elem.img_1
        })

        image.addEventListener("mouseleave",function(){
            image.src=elem.img_2
        })


        let brand1 = document.createElement("h3")
        brand1.innerText = elem.brand
        brand1.style.fontWeight = "bold"

        let description = document.createElement("p")
        description.innerText = elem.description

        let price = document.createElement("p")
        price.innerText = "$" + elem.price
        price.style.fontWeight = "bold"

        let strikedoffprice = document.createElement("p")
        strikedoffprice.innerText = "$" + elem.strikedoffprice
        strikedoffprice.style.textDecoration = "line-through"

        let discount = document.createElement("p")
        discount.innerText = "(" + elem.discount + "% OFF)"
        discount.style.color = "red"



        div1.append(price, strikedoffprice, discount)
        div2.append(brand1, description, div1)
        maindiv.append(image, div2)
        document.querySelector("#products").append(maindiv)
    })
}
